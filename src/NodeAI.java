
public class NodeAI {
	private int x;
	private int y;
	private int value;

	public NodeAI(int x, int y, int value) {
		super();
		this.x = x;
		this.y = y;
		this.value = value;
	}

	public int getX() {
		return x;
	}

	public int getY() {
		return y;
	}

	public int getValue() {
		return value;
	}
	
}
